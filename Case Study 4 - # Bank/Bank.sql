-- Metrics for SQL

A. Customer Nodes Exploration
 
  Q1. How many unique nodes are there on the Data Bank system?
 
  select count(distinct node_id) from customer_nodes;
 
 Q2. What is the number of nodes per region?

select r.region_name,count(c.node_id) from customer_nodes c inner join regions r on c.region_id =c.region_id
 group by region_name;

Q3. How many customers are allocated to each region?

select region_name,count(customer_id) as allocated_customer from customer_nodes c inner join regions r on c.region_id = r.region_id
 group by region_name;

Q4. How many days on average are customers reallocated to a different node?

with cte as (select customer_id,node_id,sum(datediff(end_date,start_date)) as days_nodes  from customer_nodes
where end_date <> '9999-10-23'
group by customer_id,node_id )
select round(avg(days_nodes),0) as avg_days_nodes from cte;

Q5. What is the median, 80th and 95th percentile for this same reallocation days metric for each region?

with cte as (select region_name,customer_id,node_id,sum(datediff(end_date,start_date)) as days_nodes  from customer_nodes c
inner join regions r on c.region_id = r.region_id
where end_date <> '9999-10-23'
group by region_name,customer_id,node_id
) ,
orderd as (
select region_name,days_nodes,
row_number () over(partition by region_name order by days_nodes) as rn
from cte),
max_rn as
(select region_name,
max(rn) as maxi from orderd
group by region_name)

select o.*,m.maxi,
round(m.maxi/2.0) as median
 from orderd o inner join max_rn m on o.region_name=m.region_name
where rn in (
round(m.maxi/2.0),
round(m.maxi*0.8,0),
round(m.maxi*0.95,0)
);
 
  B. Customer Transactions
 
  Q1.What is the unique count and total amount for each transaction type?
 
  select txn_type,
  sum(txn_amount) as total_amount,
  count(*) as unique_count_transaction
  from customer_transactions
  group by txn_type;
 
  Q2. What is the average total historical deposit counts and amounts for all customers?
 
  with cte as (
  select customer_id,txn_type,avg(txn_amount) as avg_total,count(*) as transactions from customer_transactions where txn_type = 'deposit'
  group by customer_id)
 
  select round(avg(avg_total),0) as avg_value,round(avg(transactions),0) as avg_transaction from cte;
 
  Q3. For each month - how many Data Bank customers make more than 1 deposit
  and either 1 purchase or 1 withdrawal in a single month?
 
  select  month(txn_date) as months,
  customer_id,
  (case when txn_type = 'deposit' then 1 else 0 end) as deposit,
  (case when txn_type <> 'deposit' then 1 else 0 end) as purchase_transaction
  from customer_transactions
  group by customer_id,month(txn_date)
  having sum(case when txn_type = 'deposit' then 1 else 0 end ) >1
  and  sum(case when txn_type <> 'deposit' then 1 else 0 end) = 1
 
  Q4. What is the closing balance for each customer at the end of the month?
 
  select *,
  month(txn_date) as months,
  txn_date,customer_id,
  sum(case when txn_type = 'deposit' then 1 else 0 end) - sum(case when txn_type <> 'deposit' then 1 else 0 end)  as balance,
  from customer_transactions
  where customer_id = 429
  group by month(txn_date),
  txn_date,
  customer_id
  order by months,txn_date;
 
 
Q5. What is the percentage of customers who increase their closing balance by more than 5%?

with cte as (
select 
month(txn_date) as txn_month,
txn_date,customer_id,
sum((case when txn_type = 'deposit' then txn_amount else 0 end) - (case when txn_type <> 'deposit' then txn_amount else 0 end)) as balance
from customer_transactions
group by month(txn_date),txn_date,customer_id),

balance as (
select *,
sum(balance) over(partition by customer_id order by txn_date) as running_sum,
row_number() over(partition by customer_id,txn_month order by txn_date desc) as rn 
from cte 
order by txn_date)

select customer_id,
dateadd('day',-1,dateadd('month',1,txn_month) as end_month,
dateaddd('month',-1,txn_date) as previous_month
running_sum as closing_balance 
from balance
order by end_month;